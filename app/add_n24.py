async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=24"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n24). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=24"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        topic_counts: dict[str, int] = {}

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 24, topic, question, answer, image_path, None),
            )
            inserted += 1
            topic_counts[topic] = topic_counts.get(topic, 0) + 1

        await db.commit()

        print(f"\n✅ Добавлено {inserted} заданий (task_number=24).")
        print("По темам:")
        for t, c in sorted(topic_counts.items()):
            print(f"  {t}: {c} шт.")
        print("\n💡 Задание 24 — доказательство. Ответ всегда 'доказано'.")
        print("   Проверка идёт по фото решения через нейросеть.")


if __name__ == "__main__":
    asyncio.run(main())
