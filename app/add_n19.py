async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=19"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n19). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=19"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (question, answer, image_path, topic) in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 19, topic, question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()

        single = sum(1 for _, a, _, _ in TASKS if len(a.split("|")[0]) == 1)
        multi  = sum(1 for _, a, _, _ in TASKS if len(a.split("|")[0]) == 2)
        print(f"\nГотово! Добавлено {inserted} заданий (task_number=19).")
        print(f"  Один верный ответ:  {single} заданий")
        print(f"  Два верных ответа:  {multi} заданий")
        print("Изображения не требуются — все задания текстовые.")


if __name__ == "__main__":
    asyncio.run(main())
