# Финальный список с image_path
TASKS = [(topic, question, answer, IMG_MAP.get(n))
         for (n, topic, question, answer) in RAW]

TOPIC_LABELS = {
    PARALLELEP: "I   Параллелепипед",
    PRISM:      "II  Призма",
    PYRAMID:    "III Пирамида",
    CYLINDER:   "IV  Цилиндр",
    CONE:       "V   Конус",
    SPHERE:     "VI  Шар",
}


async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type=? AND task_number=?",
            (EXAM_TYPE, TASK_NUMBER)
        ) as cur:
            count = (await cur.fetchone())[0]

        if count > 0:
            print(f"В БД уже есть {count} заданий "
                  f"(exam_type='{EXAM_TYPE}', task_number={TASK_NUMBER}).")
            if input("Удалить и перезалить? (y/n): ").strip().lower() != "y":
                print("Отмена.")
                return
            await db.execute(
                "DELETE FROM tasks WHERE exam_type=? AND task_number=?",
                (EXAM_TYPE, TASK_NUMBER)
            )
            await db.commit()
            print("Старые записи удалены.")

        inserted = 0
        missing_img: list[str] = []

        for topic, question, answer, image_path in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                (EXAM_TYPE, TASK_NUMBER, topic, question, answer, image_path, None)
            )
            inserted += 1
            if image_path and not os.path.exists(image_path):
                missing_img.append(image_path)

        await db.commit()

    print(f"\nУспешно добавлено {inserted} заданий №{TASK_NUMBER} (ЕГЭ базовый).")
    for t, label in TOPIC_LABELS.items():
        n_t = sum(1 for x in TASKS if x[0] == t)
        n_i = sum(1 for x in TASKS if x[0] == t and x[3] is not None)
        print(f"  {label:<25} {n_t:>3} зад.  (с рисунком: {n_i})")

    n_unique = len({x[3] for x in TASKS if x[3] is not None})
    print(f"\nВсего задач: {len(TASKS)}  |  уникальных изображений: {n_unique}")

    if missing_img:
        print(f"\n⚠  Отсутствует {len(missing_img)} файлов в {IMG_DIR}/")
        for p in sorted(set(missing_img))[:15]:
            print(f"   {p}")
    else:
        print(f"\n✅  Все изображения найдены в {IMG_DIR}/")


if __name__ == "__main__":
    asyncio.run(main())
