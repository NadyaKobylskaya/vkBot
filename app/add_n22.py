async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=22"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n22). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=22"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        img_count = 0

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 22, topic, question, answer, image_path, None),
            )
            inserted += 1
            if image_path:
                img_count += 1

        await db.commit()

        print(f"\n✅ Добавлено {inserted} заданий (task_number=22).")
        print(f"   Из них с изображениями: {img_count}")
        print(f"   Без изображений (текстовые): {inserted - img_count}")
        print(f"\n📁 Нужно создать {img_count} изображений в:")
        print(f"   {os.path.join(BASE_DIR, IMG_DIR)}")
        print(f"\nМаппинг изображений:")
        print(f"  img_001–img_006  → Блок I  (кусочные линейные, задачи 1–6)")
        print(f"  img_007–img_014  → Блок II (кусочные квадратичные, задачи 7–14)")
