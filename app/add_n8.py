async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")
    by_topic = {}
    for _, _, t in TASKS:
        by_topic[t] = by_topic.get(t, 0) + 1
    for t, n in by_topic.items():
        print(f"  {t}: {n}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=8"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"\nВ БД уже есть {count} записей (task_number=8). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=8"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for question, answer, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 8, topic, question, answer, None, None),
            )
            inserted += 1

        await db.commit()

    print(f"\n✅ Добавлено {inserted} заданий (exam_type='oge', task_number=8).")
    print("Изображения не требуются — все задания текстовые.")


if __name__ == "__main__":
    asyncio.run(main())