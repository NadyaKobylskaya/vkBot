async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND topic='shiny'"
        ) as cursor:
            count = (await cursor.fetchone())[0]

        if count > 0:
            print(f"В БД уже есть {count} заданий по теме 'shiny'.")
            ans = input("Удалить и перезалить? (y/n): ").strip().lower()
            if ans == 'y':
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND topic='shiny'"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (set_id, task_num, question, answer) in TASKS:
            if task_num == 1:
                full_q = DESC[set_id] + "\n\n" + question
                img    = f"{RIS1}|{RIS2}|{TABLES[set_id]}"
            else:
                full_q = question
                img    = None

            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ('oge', task_num, 'shiny', full_q, answer, img, set_id)
            )
            inserted += 1

        await db.commit()
        print(f"Успешно добавлено {inserted} заданий по теме 'shiny'.")


if __name__ == "__main__":
    asyncio.run(main())
