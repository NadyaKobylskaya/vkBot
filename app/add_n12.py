async def main():
    print(f"Подключаемся к БД: {DB_PATH}")

    # Проверяем наличие картинок
    missing = [p for p in (IMG_COND, IMG_KIN, IMG_QUAD) if not os.path.exists(p)]
    if missing:
        for p in missing:
            print(f"⚠️  Картинка не найдена: {p}")
        print("   Запустите сначала: python create_n12_images.py")
        return

    async with aiosqlite.connect(DB_PATH) as db:

        # ── проверка дублей ─────────────────────────────────────────────
        async with db.execute(
            "SELECT COUNT(*) FROM tasks "
            "WHERE exam_type='oge' AND task_number=12 AND topic='general'"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            answer = input(
                f"В БД уже есть {count} задание(й) 12 с topic='general'. "
                "Удалить и перезаписать? (y/n): "
            ).strip().lower()
            if answer == "y":
                await db.execute(
                    "DELETE FROM tasks "
                    "WHERE exam_type='oge' AND task_number=12 AND topic='general'"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Операция отменена.")
                return

        # ── вставка ─────────────────────────────────────────────────────
        inserted = 0
        for question, answer, image_path in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 12, "general", question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()
        print(f"✅ Добавлено {inserted} заданий (задание 12, topic='general').")
        print("   Задачи 31–42: картинка formula_condenser.png / formula_kinetic.png")
        print("   Задачи 55–60: картинка formula_quad.png")


if __name__ == "__main__":
    asyncio.run(main())

