async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks "
            "WHERE exam_type='oge' AND task_number=16 AND topic='circles'"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n16, circles). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks "
                    "WHERE exam_type='oge' AND task_number=16 AND topic='circles'"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (num, question, answer, img_n) in TASKS:
            image_path = img(img_n)
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 16, "circles", question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()
        print(f"Готово! Добавлено {inserted} заданий (task_number=16, topic='circles').")
        print(f"Изображения ожидаются в: {IMG_DIR}/img_01.png … img_61.png")

        # Проверка наличия картинок
        missing = []
        seen = set()
        for (_, _, _, img_n) in TASKS:
            if img_n not in seen:
                seen.add(img_n)
                full_path = os.path.join(BASE_DIR, img(img_n))
                if not os.path.exists(full_path):
                    missing.append(img(img_n))
        if missing:
            print(f"⚠️ Не найдено {len(missing)} картинок:")
            for p in missing:
                print(f"   {p}")
        else:
            print("✅ Все изображения на месте.")


if __name__ == "__main__":
    asyncio.run(main())
