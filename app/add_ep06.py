async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='ege_profile' AND task_number=6"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей для задания 6. "
                f"Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='ege_profile' AND task_number=6"
                )
                await db.commit()
                print(f"Удалено {count} старых записей.")
            else:
                print("Отмена.")
                return

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("ege_profile", 6, topic, question, answer, image_path, None),
            )
        await db.commit()
        print(f"✅ Добавлено {len(TASKS)} заданий (ege_profile, task_number=6).")


if __name__ == "__main__":
    asyncio.run(main())
