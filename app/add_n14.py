    async with aiosqlite.connect(DB_PATH) as db:

        for topic in ("arithmetic_progression", "geometric_progression"):
            async with db.execute(
                "SELECT COUNT(*) FROM tasks "
                "WHERE exam_type='oge' AND task_number=14 AND topic=?", (topic,)
            ) as cur:
                (count,) = await cur.fetchone()

            if count > 0:
                ans = input(
                    f"В БД уже есть {count} задание(й) 14 с topic='{topic}'. "
                    f"Удалить и перезаписать? (y/n): "
                ).strip().lower()
                if ans == "y":
                    await db.execute(
                        "DELETE FROM tasks WHERE exam_type='oge' AND task_number=14 AND topic=?",
                        (topic,)
                    )
                    await db.commit()
                    print(f"  Старые записи ({topic}) удалены.")
                else:
                    print(f"  Пропускаем topic='{topic}'.")

        inserted = {"arithmetic_progression": 0, "geometric_progression": 0}
        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 14, topic, question, answer, image_path, None),
            )
            inserted[topic] += 1

        await db.commit()
        print(f"✅ Добавлено: арифм. {inserted['arithmetic_progression']} зад., "
              f"геом. {inserted['geometric_progression']} зад.")
        print("   Картинки: задание 14 (столики) и задание 18 (змейка) из Блока 2.")


