# ЗАПИСЬ В БД
# ════════════════════════════════════════════════════════════════════════════

async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")
    print(f"Все с изображениями: {sum(1 for t in TASKS if t[2])}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='ege_base' AND task_number=9"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей для задания 9 ЕГЭ база. "
                f"Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='ege_base' AND task_number=9"
                )
                await db.commit()
                print(f"Удалено {count} старых записей.")
            else:
                print("Отмена.")
                return

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("ege_base", 9, topic, question, answer, image_path, None),
            )
        await db.commit()
        print(f"✅ Добавлено {len(TASKS)} заданий (ege_base, task_number=9).")


if __name__ == "__main__":
    asyncio.run(main())
