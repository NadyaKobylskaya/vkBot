import asyncio
import aiosqlite
import os

_dir = os.path.dirname(os.path.abspath(__file__))
BASE_DIR = os.path.dirname(_dir)
DB_PATH = os.path.join(BASE_DIR, "app", "bot_database.db")

TASKS: list[tuple] = []
TOPIC = "ege_b09_area"
IMG = "app/images_b/n9"

_INTRO = (
    "План местности разбит на клетки. Каждая клетка обозначает квадрат "
    "1 м × 1 м. Найдите площадь участка, изображённого на плане. "
    "Ответ дайте в квадратных метрах."
)


def add(q: str, a: str, i: str):
    TASKS.append((q, a, i, TOPIC))


def img(name: str) -> str:
    return f"{IMG}/{name}.png"


# ════════════════════════════════════════════════════════════════════════════
# Задание 1 — Прямоугольные ступенчатые фигуры (6 вариантов)
# ════════════════════════════════════════════════════════════════════════════

for n, ans in enumerate(["10", "14", "8", "10", "15", "20"], start=1):
    add(_INTRO, ans, img(f"img_01_{n}"))

# ════════════════════════════════════════════════════════════════════════════
# Задание 2 — Параллелограммы (5 вариантов)
# ════════════════════════════════════════════════════════════════════════════

for n, ans in enumerate(["6", "9", "8", "4", "16"], start=1):
    add(_INTRO, ans, img(f"img_02_{n}"))

