async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=18"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n18). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=18"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (question, answer, image_path, topic) in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 18, topic, question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()
        print(f"\nГотово! Добавлено {inserted} заданий (task_number=18).")
        print(f"Изображения ожидаются в: {IMG_DIR}/img_001.png … img_144.png")

        # Проверка наличия картинок
        missing = []
        for (_, _, image_path, _) in TASKS:
            full_path = os.path.join(BASE_DIR, image_path)
            if not os.path.exists(full_path):
                missing.append(image_path)
        if missing:
            print(f"\n⚠️ Не найдено {len(missing)} картинок:")
            for p in missing:
                print(f"   {p}")
        else:
            print("✅ Все изображения на месте.")


if __name__ == "__main__":
    asyncio.run(main())
