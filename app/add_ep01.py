# ЗАПИСЬ В БД
# ════════════════════════════════════════════════════════════════════════════

async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач: {len(TASKS)}")

    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='ege_profile' AND task_number=1"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(f"В БД уже есть {count} записей. Удалить и загрузить заново? [y/N]: ").strip().lower()
            if ans == "y":
                await db.execute("DELETE FROM tasks WHERE exam_type='ege_profile' AND task_number=1")
                await db.commit()
            else:
                print("Отмена."); return

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks (exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("ege_profile", 1, topic, question, answer, image_path, None),
            )
        await db.commit()
        print(f"✅ Добавлено {len(TASKS)} заданий (ege_profile, task_number=1).")

if __name__ == "__main__":
    asyncio.run(main())
