EXAM_TYPE = "ege_base"
TASK_NUMBER = 18


async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:

        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type=? AND task_number=?",
            (EXAM_TYPE, TASK_NUMBER),
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (egb, n18). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type=? AND task_number=?",
                    (EXAM_TYPE, TASK_NUMBER),
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        topic_counts: dict[str, int] = {}

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                (EXAM_TYPE, TASK_NUMBER, topic, question, answer, image_path, None),
            )
            inserted += 1
            topic_counts[topic] = topic_counts.get(topic, 0) + 1

        await db.commit()

        print(f"\n✅ Добавлено {inserted} заданий (exam_type='ege_base', task_number=18).")
        print("По темам:")
        for t, c in sorted(topic_counts.items()):
            print(f"  {t}: {c} заданий")

        # Проверка картинок
        missing = []
        seen: set[str] = set()
        for _, _, image_path, _ in TASKS:
            if image_path and image_path not in seen:
                seen.add(image_path)
                full_path = os.path.join(BASE_DIR, image_path)
                if not os.path.exists(full_path):
                    missing.append(image_path)
        if missing:
            print(f"\n⚠️  Нет {len(missing)} картинок — нужно добавить:")
            for p in missing:
                print(f"   {p}")
        else:
            print("\n✅ Все изображения на месте.")

        print("\nКарта изображений (images_b/n18/):")
        print("  b18_01..06.png  — Блок 18: показательные (числовые прямые)")
        print("  b21_01..07.png  — Блок 21: логарифмические (числовые прямые)")
        print("  b24_01..09.png  — Блок 24: смешанные (числовые прямые)")


if __name__ == "__main__":
    asyncio.run(main())
