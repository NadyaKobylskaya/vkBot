async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Задач подготовлено: {len(TASKS)}")

    cnt = Counter(t[3] for t in TASKS)
    for task_num in sorted(cnt):
        print(f"  Задание №{task_num}: {cnt[task_num]} задач")

    async with aiosqlite.connect(DB_PATH) as db:
        inserted = 0
        for question, answer, topic, task_number, image_path in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", task_number, topic, question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()

    print(f"\n✅ Добавлено {inserted} заданий (старые сохранены).")
    print("\n📁 Разложите изображения по папкам (одно фото на тип задачи):")
    all_images = sorted(set(t[4] for t in TASKS if t[4]))
    for img in all_images:
        print(f"   {img}")


if __name__ == "__main__":
    asyncio.run(main())
