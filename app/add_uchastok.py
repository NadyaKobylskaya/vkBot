async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        cur = await db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' "
            "AND task_number BETWEEN 1 AND 5 AND topic='uchastok'"
        )
        count = (await cur.fetchone())[0]
        if count > 0:
            print(f"⚠️  Уже есть {count} заданий по теме 'uchastok'.")
            ans = input("Удалить и перезалить? (да/нет): ").strip().lower()
            if ans == "да":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' "
                    "AND task_number BETWEEN 1 AND 5 AND topic='uchastok'"
                )
                await db.commit()
                print("🗑  Удалено.")
            else:
                print("Отмена.")
                return

        total = 0
        for set_id, task_num, context, question, answer, plan, table_img in TASKS:
            if task_num == 1:
                full_q = context + question
                img = f"{plan}|{table_img}" if table_img else plan
            else:
                full_q = question
                img = None
            await db.execute(
                "INSERT INTO tasks (exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", task_num, "uchastok", full_q, answer, img, set_id)
            )
            total += 1
        await db.commit()
        print(f"✅ Добавлено {total} заданий — 14 наборов × 5, тема 'uchastok'")

asyncio.run(main())
