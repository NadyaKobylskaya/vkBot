async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=17"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n17). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=17"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (question, answer, image_path, topic) in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 17, topic, question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()

        topic_counts: dict[str, int] = {}
        for (_, _, _, t) in TASKS:
            topic_counts[t] = topic_counts.get(t, 0) + 1

        print(f"\nГотово! Добавлено {inserted} заданий (task_number=17).")
        print("По темам:")
        for t, c in sorted(topic_counts.items()):
            print(f"  {t}: {c}")
        print(f"\nИзображения ожидаются в: {IMG_DIR}/")
        print("  img_01.png … img_04.png   (задания 1–8, по паре на картинку)")
        print("  img_05a.png, img_05b.png  (задания 9, 10 — разные картинки)")
        print("  img_06a.png, img_06b.png  (задания 11, 12 — разные картинки)")
        print("  img_07a.png, img_07b.png  (задания 13, 14 — разные картинки)")
        print("  img_08a.png, img_08b.png  (задания 15, 16 — разные картинки)")
        print("  img_09.png … img_61.png   (задания 17–112)")

        missing = []
        seen: set[str] = set()
        for (_, _, image_path, _) in TASKS:
            if image_path not in seen:
                seen.add(image_path)
                full_path = os.path.join(BASE_DIR, image_path)
                if not os.path.exists(full_path):
                    missing.append(image_path)
        if missing:
            print(f"\n⚠️ Не найдено {len(missing)} картинок:")
            for p in missing:
                print(f"   {p}")
        else:
            print("\n✅ Все изображения на месте.")


if __name__ == "__main__":
    asyncio.run(main())