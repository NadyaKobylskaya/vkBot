def fmt_answer(value) -> str:
    value = sp.simplify(value)
    if value.has(sp.pi):
        return str(value).replace("pi", "π").replace("*", "")
    if value.is_Rational:
        if value.q == 1:
            return str(value.p)
        q = value.q
        t = q
        for p in (2, 5):
            while t % p == 0:
                t //= p
        if t == 1:
            return ("%.10f" % float(value)).rstrip("0").rstrip(".")
        return f"{value.p}/{value.q}"
    return ("%.10f" % float(sp.N(value))).rstrip("0").rstrip(".")


def add(question: str, answer, image_path=None):
    TASKS.append((question, fmt_answer(answer), image_path, TOPIC))


def critical_point(expr, kind: str):
    roots = [sp.simplify(r) for r in sp.solve(sp.diff(expr, x), x) if r.is_real]
    if kind == "max":
        good = [r for r in roots if float(sp.N(sp.diff(expr, x, 2).subs(x, r))) < 0]
    else:
        good = [r for r in roots if float(sp.N(sp.diff(expr, x, 2).subs(x, r))) > 0]
    if not good:
        raise ValueError(f"Не удалось найти точку {kind}: {expr}")
    return good[0]


def extreme_value(expr, kind: str, a, b):
    a = sp.sympify(a)
    b = sp.sympify(b)
    points = [a, b]
    for r in sp.solve(sp.diff(expr, x), x):
        if r.is_real and float(sp.N(a)) <= float(sp.N(r)) <= float(sp.N(b)):
            points.append(sp.simplify(r))
    vals = [sp.simplify(expr.subs(x, p)) for p in points]
    return max(vals, key=lambda v: float(sp.N(v))) if kind == "max" else min(vals, key=lambda v: float(sp.N(v)))


def add_point(kind_ru: str, expr_text: str, expr):
    kind = "max" if "максимума" in kind_ru else "min"
    add(f"Найдите точку {kind_ru} функции y = {expr_text}.", critical_point(expr, kind))


def add_value(kind_ru: str, expr_text: str, expr, interval_text: str, a, b):
    kind = "max" if "наибольшее" in kind_ru else "min"
    add(f"Найдите {kind_ru} значение функции y = {expr_text} на отрезке {interval_text}.", extreme_value(expr, kind, a, b))


