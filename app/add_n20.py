async def main() -> None:
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=20"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n20). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=20"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        topic_counts: dict[str, int] = {}

        for (question, answer, image_path, topic) in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 20, topic, question, answer, image_path, None),
            )
            inserted += 1
            topic_counts[topic] = topic_counts.get(topic, 0) + 1

        await db.commit()

        print(f"\nГотово! Добавлено {inserted} заданий (task_number=20).")
        print("По темам:")
        for t, c in sorted(topic_counts.items()):
            print(f"  {t}: {c}")

        # Проверка изображений
        missing = []
        seen: set[str] = set()
        for (_, _, image_path, _) in TASKS:
            if image_path and image_path not in seen:
                seen.add(image_path)
                full_path = os.path.join(BASE_DIR, image_path)
                if not os.path.exists(full_path):
                    missing.append(image_path)
        if missing:
            print(f"\n⚠️ Не найдено {len(missing)} картинок:")
            for p in missing:
                print(f"   {p}")
        else:
            print("\n✅ Все изображения на месте (или не требуются).")

        print("\nКарта изображений (app/images/n20/):")
        print("  img_001–008  Файл 1, Задание 1  (выражения с дробями)")
