    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='ege_base' AND task_number=19"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей для задания 19. "
                f"Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='ege_base' AND task_number=19"
                )
                await db.commit()
                print(f"Удалено {count} старых записей.")
            else:
                print("Отмена.")
                return

        for question, answer, image_path, topic in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("ege_base", 19, topic, question, answer, image_path, None),
            )
        await db.commit()
        print(f"✅ Добавлено {len(TASKS)} заданий.")


