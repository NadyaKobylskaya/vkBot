    async with aiosqlite.connect(DB_PATH) as db:
        # Проверяем наличие уже загруженных задач n15
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number=15 AND topic='triangles'"
        ) as cur:
            (count,) = await cur.fetchone()

        if count > 0:
            ans = input(
                f"В БД уже есть {count} записей (n15, triangles). "
                "Удалить и загрузить заново? [y/N]: "
            ).strip().lower()
            if ans == "y":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number=15 AND topic='triangles'"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        for (num, question, answer, img_n) in TASKS:
            image_path = img(img_n)
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", 15, "triangles", question, answer, image_path, None),
            )
            inserted += 1

        await db.commit()
        print(f"Готово! Добавлено {inserted} заданий (task_number=15, topic='triangles').")
