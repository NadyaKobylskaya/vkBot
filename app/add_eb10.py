TOPIC_LABELS = {
    PERIMETER:    "Периметр",
    AREA:         "Площадь",
    PYTHAGORAS:   "Теорема Пифагора",
    SIMILAR:      "Подобные треугольники",
    CIRCLE:       "Окружность",
    TRAPEZOID_ML: "Средняя линия трапеции",
}


async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type=? AND task_number=?",
            (EXAM_TYPE, TASK_NUMBER)
        ) as cursor:
            count = (await cursor.fetchone())[0]

        if count > 0:
            print(f"В БД уже есть {count} заданий "
                  f"(exam_type='{EXAM_TYPE}', task_number={TASK_NUMBER}).")
            ans = input("Удалить и перезалить? (y/n): ").strip().lower()
            if ans == 'y':
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type=? AND task_number=?",
                    (EXAM_TYPE, TASK_NUMBER)
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        inserted = 0
        missing_img: list[str] = []

        for (topic, question, answer, image_path) in TASKS:
            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                (EXAM_TYPE, TASK_NUMBER, topic, question, answer, image_path, None)
            )
            inserted += 1
            if image_path and not os.path.exists(image_path):
                missing_img.append(image_path)

        await db.commit()

    print(f"\nУспешно добавлено {inserted} заданий №{TASK_NUMBER} (ЕГЭ базовый).")
    for t, label in TOPIC_LABELS.items():
        n_total  = sum(1 for task in TASKS if task[0] == t)
        n_img    = sum(1 for task in TASKS if task[0] == t and task[3] is not None)
        print(f"  {label:<32} {n_total:>3} зад.  (с рисунком: {n_img})")

    total_img = sum(1 for task in TASKS if task[3] is not None)
    unique_paths = len({task[3] for task in TASKS if task[3] is not None})
    print(f"\nВсего задач с image_path: {total_img}  |  уникальных файлов: {unique_paths}")

    if missing_img:
        print(f"\n⚠  Отсутствует {len(missing_img)} файлов изображений.")
        print(f"   Положи нарезанные PNG в папку: {IMG_DIR}/")
        for p in sorted(set(missing_img))[:12]:
            print(f"   {p}")
        if len(set(missing_img)) > 12:
            print(f"   … и ещё {len(set(missing_img))-12} файлов")
    else:
        print(f"\n✅  Все изображения найдены в {IMG_DIR}/")


if __name__ == "__main__":
    asyncio.run(main())
