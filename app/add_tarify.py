async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        # ── Проверка дублей ───────────────────────────────────────────────────
        async with db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND topic='tarify'"
        ) as cursor:
            count = (await cursor.fetchone())[0]

        if count > 0:
            print(f"В БД уже есть {count} заданий по теме 'tarify'.")
            ans = input("Удалить и перезалить? (y/n): ").strip().lower()
            if ans == 'y':
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND topic='tarify'"
                )
                await db.commit()
                print("Старые записи удалены.")
            else:
                print("Отмена.")
                return

        # ── Вставка ───────────────────────────────────────────────────────────
        inserted = 0
        for (set_id, task_num, question, answer, img_override) in TASKS:
            if task_num == 1:
                full_q = DESC[set_id] + "\n\n" + question
                img    = GRAPHS[set_id]
            else:
                full_q = question
                img    = TABLES[set_id] if img_override == 'table' else None

            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ('oge', task_num, 'tarify', full_q, answer, img, set_id)
            )
            inserted += 1

        await db.commit()
        print(f"Успешно добавлено {inserted} заданий по теме 'tarify'.")


if __name__ == "__main__":
    asyncio.run(main())
