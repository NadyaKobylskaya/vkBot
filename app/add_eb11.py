def _frac(num: int, den: int) -> Fraction:
    return Fraction(num, den)

def _fmt(x) -> str:
    """Целое число или минимальная дробь без float-мусора."""
    if isinstance(x, Fraction):
        if x.denominator == 1:
            return str(x.numerator)
        result = float(x)
    else:
        result = float(x)
    rounded = round(result)
    if abs(result - rounded) < 1e-9:
        return str(rounded)
    return str(round(result, 6))

