async def main() -> None:
    print(f"База данных: {DB_PATH}")
    print(f"Записей к обновлению: {len(ANSWERS)}")

    async with aiosqlite.connect(DB_PATH) as db:

        cursor = await db.execute(
            "SELECT COUNT(*) FROM tasks "
            "WHERE exam_type=\'ege\' AND task_number=13"
        )
        (count,) = await cursor.fetchone()
        if count == 0:
            print("⚠️  Задания ЕГЭ №13 не найдены. Сначала запустите add_ege13.py")
            return
        print(f"Найдено заданий в БД: {count}")

        updated, not_found = 0, []

        for img_num, answer in ANSWERS.items():
            image_path = img(img_num)
            result = await db.execute(
                "UPDATE tasks SET answer = ? "
                "WHERE exam_type=\'ege\' AND task_number=13 AND image_path=?",
                (answer, image_path)
            )
            if result.rowcount > 0:
                updated += 1
            else:
                not_found.append(img_num)

        await db.commit()

    print(f"\n✅ Обновлено: {updated} из {len(ANSWERS)}")
    if not_found:
        print(f"⚠️  Не найдены: img {not_found}")
    else:
        print("✅ Все ответы записаны.")

    # Проверка пустых
    async with aiosqlite.connect(DB_PATH) as db:
        cursor = await db.execute(
            "SELECT COUNT(*) FROM tasks "
            "WHERE exam_type=\'ege\' AND task_number=13 "
            "AND (answer IS NULL OR answer=\'\' )"
        )
        (empty,) = await cursor.fetchone()
        if empty:
            print(f"⚠️  Осталось без ответа: {empty}")
        else:
            print("✅ Все задания имеют ответ.")


if __name__ == "__main__":
    asyncio.run(main())
