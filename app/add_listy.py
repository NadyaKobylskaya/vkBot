async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        cursor = await db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' AND task_number BETWEEN 1 AND 5 AND topic='listy'"
        )
        count = (await cursor.fetchone())[0]
        if count > 0:
            print(f"⚠️  Уже есть {count} заданий по теме 'listy'.")
            ans = input("Удалить и перезалить? (да/нет): ").strip().lower()
            if ans == "да":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' AND task_number BETWEEN 1 AND 5 AND topic='listy'"
                )
                await db.commit()
                print("🗑  Старые задания удалены.")
            else:
                print("Отмена.")
                return

        total = 0
        for set_id, task_number, question, answer, table_image in TASKS:
            # Задание №1: полное условие + схема + таблица
            # Задания №2-5: только вопрос, без условия и картинок
            if task_number == 1:
                full_question = CONTEXT + question
                if table_image:
                    image_path = f"{SCHEMA_IMAGE}|{table_image}"
                else:
                    image_path = SCHEMA_IMAGE
            else:
                full_question = question
                image_path = None

            await db.execute("""
                INSERT INTO tasks (exam_type, task_number, topic, question, answer, image_path, set_id)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, ("oge", task_number, "listy", full_question, answer, image_path, set_id))
            total += 1

        await db.commit()
        print(f"✅ Добавлено {total} заданий — 14 наборов × 5, тема 'listy'")
        print("📁 Картинки клади в: app/images/listy_setN_task1.png (N = 1..14)")


asyncio.run(main())