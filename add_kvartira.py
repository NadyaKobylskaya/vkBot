async def main():
    async with aiosqlite.connect(DB_PATH) as db:
        cur = await db.execute(
            "SELECT COUNT(*) FROM tasks WHERE exam_type='oge' "
            "AND task_number BETWEEN 1 AND 5 AND topic='kvartira'"
        )
        count = (await cur.fetchone())[0]
        if count > 0:
            print(f"⚠️  Уже есть {count} заданий по теме 'kvartira'.")
            ans = input("Удалить и перезалить? (да/нет): ").strip().lower()
            if ans == "да":
                await db.execute(
                    "DELETE FROM tasks WHERE exam_type='oge' "
                    "AND task_number BETWEEN 1 AND 5 AND topic='kvartira'"
                )
                await db.commit()
                print("🗑  Удалено.")
            else:
                print("Отмена.")
                return

        total = 0
        for set_id, task_num, question, answer, img_override in TASKS:
            if task_num == 1:
                full_q = C + DESC[set_id] + question
                img = PLANS[set_id]
            else:
                full_q = question
                img = img_override  # None для заданий 2-4, путь к таблице для задания 5

            await db.execute(
                "INSERT INTO tasks "
                "(exam_type, task_number, topic, question, answer, image_path, set_id) "
                "VALUES (?, ?, ?, ?, ?, ?, ?)",
                ("oge", task_num, "kvartira", full_q, answer, img, set_id)
            )
            total += 1

        await db.commit()
        print(f"✅ Добавлено {total} заданий — 12 наборов × 5, тема 'kvartira'.")


asyncio.run(main())
